# ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê

def pluralize(
    count: int,
    forms: tuple,
    lang: str = "ru"
) -> str:
    """
    –°–∫–ª–æ–Ω–µ–Ω–∏–µ —Å—É—â–µ—Å—Ç–≤–∏—Ç–µ–ª—å–Ω—ã—Ö.
    
    Args:
        count: –ß–∏—Å–ª–æ
        forms: –§–æ—Ä–º—ã —Å–ª–æ–≤–∞ (–¥–ª—è ru: –æ–¥–∏–Ω, –¥–≤–∞, –º–Ω–æ–≥–æ; –¥–ª—è en: –µ–¥–∏–Ω—Å—Ç–≤–µ–Ω–Ω–æ–µ, –º–Ω–æ–∂–µ—Å—Ç–≤–µ–Ω–Ω–æ–µ)
        lang: –Ø–∑—ã–∫
        
    Returns:
        –ü—Ä–∞–≤–∏–ª—å–Ω–∞—è —Ñ–æ—Ä–º–∞ —Å–ª–æ–≤–∞
        
    Example:
        pluralize(5, ("–ø–æ–¥–ø–∏—Å–∫–∞", "–ø–æ–¥–ø–∏—Å–∫–∏", "–ø–æ–¥–ø–∏—Å–æ–∫"), "ru") -> "–ø–æ–¥–ø–∏—Å–æ–∫"
        pluralize(5, ("subscription", "subscriptions"), "en") -> "subscriptions"
    """
    if lang == "ru" and len(forms) == 3:
        if count % 10 == 1 and count % 100 != 11:
            return forms[0]
        elif count % 10 in (2, 3, 4) and count % 100 not in (12, 13, 14):
            return forms[1]
        else:
            return forms[2]
    elif len(forms) >= 2:
        return forms[0] if count == 1 else forms[1]
    return forms[0]


def mask_token(token: str, visible_chars: int = 4) -> str:
    """
    –ú–∞—Å–∫–∏—Ä–æ–≤–∞–Ω–∏–µ —Ç–æ–∫–µ–Ω–∞ –¥–ª—è –±–µ–∑–æ–ø–∞—Å–Ω–æ–≥–æ –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è.
    
    Args:
        token: –¢–æ–∫–µ–Ω
        visible_chars: –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–∏–¥–∏–º—ã—Ö —Å–∏–º–≤–æ–ª–æ–≤ —Å –∫–∞–∂–¥–æ–π —Å—Ç–æ—Ä–æ–Ω—ã
        
    Returns:
        –ó–∞–º–∞—Å–∫–∏—Ä–æ–≤–∞–Ω–Ω—ã–π —Ç–æ–∫–µ–Ω: "1234...wxyz"
    """
    if not token:
        return ""
    
    if len(token) <= visible_chars * 2:
        return "*" * len(token)
    
    return token[:visible_chars] + "..." + token[-visible_chars:]


