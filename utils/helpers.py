def format_price(
    amount: Union[float, Decimal, int],
    currency: str = "USDT",
    decimals: int = 2
) -> str:
    """
    –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Ü–µ–Ω—ã.
    
    Args:
        amount: –°—É–º–º–∞
        currency: –í–∞–ª—é—Ç–∞ (USDT, USD, RUB)
        decimals: –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –∑–Ω–∞–∫–æ–≤ –ø–æ—Å–ª–µ –∑–∞–ø—è—Ç–æ–π
        
    Returns:
        –û—Ç—Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω–∞—è —Ü–µ–Ω–∞: "$10.00" –∏–ª–∏ "10.00 USDT"
    """
    if isinstance(amount, Decimal):
        amount = float(amount)
    
    formatted = f"{amount:,.{decimals}f}".replace(",", " ")
    
    if currency.upper() in ("USD", "$"):
        return f"${formatted}"
    elif currency.upper() == "RUB":
        return f"{formatted} ‚ÇΩ"
    elif currency.upper() == "EUR":
        return f"‚Ç¨{formatted}"
    else:
        return f"{formatted} {currency}"


