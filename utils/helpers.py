def format_duration(days: int, lang: str = "ru") -> str:
    """
    –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –¥–ª–∏—Ç–µ–ª—å–Ω–æ—Å—Ç–∏ –ø–æ–¥–ø–∏—Å–∫–∏.
    
    Args:
        days: –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–Ω–µ–π
        lang: –Ø–∑—ã–∫ (ru/en)
        
    Returns:
        "30 –¥–Ω–µ–π" –∏–ª–∏ "30 days"
    """
    if lang == "ru":
        # –°–∫–ª–æ–Ω–µ–Ω–∏–µ –¥–ª—è —Ä—É—Å—Å–∫–æ–≥–æ —è–∑—ã–∫–∞
        if days % 10 == 1 and days % 100 != 11:
            return f"{days} –¥–µ–Ω—å"
        elif days % 10 in (2, 3, 4) and days % 100 not in (12, 13, 14):
            return f"{days} –¥–Ω—è"
        else:
            return f"{days} –¥–Ω–µ–π"
    else:
        # –ê–Ω–≥–ª–∏–π—Å–∫–∏–π
        if days == 1:
            return f"{days} day"
        return f"{days} days"


def format_time_remaining(expires_at: datetime, lang: str = "ru") -> str:
    """
    –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –æ—Å—Ç–∞–≤—à–µ–≥–æ—Å—è –≤—Ä–µ–º–µ–Ω–∏.
    
    Args:
        expires_at: –î–∞—Ç–∞ –∏—Å—Ç–µ—á–µ–Ω–∏—è
        lang: –Ø–∑—ã–∫
        
    Returns:
        "3 –¥–Ω—è", "5 —á–∞—Å–æ–≤", "–∏—Å—Ç–µ–∫–ª–∞"
    """
    now = datetime.utcnow()
    
    if expires_at <= now:
        return "–∏—Å—Ç–µ–∫–ª–∞" if lang == "ru" else "expired"
    
    delta = expires_at - now
    days = delta.days
    hours = delta.seconds // 3600
    
    if days > 0:
        return format_duration(days, lang)
    elif hours > 0:
        if lang == "ru":
            if hours % 10 == 1 and hours % 100 != 11:
                return f"{hours} —á–∞—Å"
            elif hours % 10 in (2, 3, 4) and hours % 100 not in (12, 13, 14):
                return f"{hours} —á–∞—Å–∞"
            else:
                return f"{hours} —á–∞—Å–æ–≤"
        else:
            return f"{hours} hour{'s' if hours != 1 else ''}"
    else:
        minutes = delta.seconds // 60
        if lang == "ru":
            return f"{minutes} –º–∏–Ω"
        return f"{minutes} min"


