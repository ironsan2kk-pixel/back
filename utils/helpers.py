def calculate_discount(
    original_price: float,
    discount_percent: float = 0,
    discount_amount: float = 0
) -> float:
    """
    –†–∞—Å—á—ë—Ç —Ü–µ–Ω—ã —Å–æ —Å–∫–∏–¥–∫–æ–π.
    
    Args:
        original_price: –ò—Å—Ö–æ–¥–Ω–∞—è —Ü–µ–Ω–∞
        discount_percent: –ü—Ä–æ—Ü–µ–Ω—Ç —Å–∫–∏–¥–∫–∏
        discount_amount: –§–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–Ω–∞—è —Å–∫–∏–¥–∫–∞
        
    Returns:
        –¶–µ–Ω–∞ —Å–æ —Å–∫–∏–¥–∫–æ–π (–º–∏–Ω–∏–º—É–º 0)
    """
    price = original_price
    
    # –ü—Ä–∏–º–µ–Ω—è–µ–º –ø—Ä–æ—Ü–µ–Ω—Ç–Ω—É—é —Å–∫–∏–¥–∫—É
    if discount_percent > 0:
        price = price * (100 - discount_percent) / 100
    
    # –ü—Ä–∏–º–µ–Ω—è–µ–º —Ñ–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–Ω—É—é —Å–∫–∏–¥–∫—É
    if discount_amount > 0:
        price = price - discount_amount
    
    return max(0, round(price, 2))


def calculate_percentage(part: int, total: int, decimals: int = 1) -> float:
    """
    –†–∞—Å—á—ë—Ç –ø—Ä–æ—Ü–µ–Ω—Ç–∞.
    
    Args:
        part: –ß–∞—Å—Ç—å
        total: –¶–µ–ª–æ–µ
        decimals: –ó–Ω–∞–∫–æ–≤ –ø–æ—Å–ª–µ –∑–∞–ø—è—Ç–æ–π
        
    Returns:
        –ü—Ä–æ—Ü–µ–Ω—Ç
    """
    if total == 0:
        return 0.0
    return round(part / total * 100, decimals)


