def generate_random_string(
    length: int = 8,
    chars: str = None,
    uppercase: bool = True
) -> str:
    """
    –ì–µ–Ω–µ—Ä–∞—Ü–∏—è —Å–ª—É—á–∞–π–Ω–æ–π —Å—Ç—Ä–æ–∫–∏.
    
    Args:
        length: –î–ª–∏–Ω–∞ —Å—Ç—Ä–æ–∫–∏
        chars: –ù–∞–±–æ—Ä —Å–∏–º–≤–æ–ª–æ–≤ (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é –±—É–∫–≤—ã –∏ —Ü–∏—Ñ—Ä—ã)
        uppercase: –ü—Ä–∏–≤–æ–¥–∏—Ç—å –∫ –≤–µ—Ä—Ö–Ω–µ–º—É —Ä–µ–≥–∏—Å—Ç—Ä—É
        
    Returns:
        –°–ª—É—á–∞–π–Ω–∞—è —Å—Ç—Ä–æ–∫–∞
    """
    if chars is None:
        chars = string.ascii_letters + string.digits
    
    result = ''.join(random.choices(chars, k=length))
    
    if uppercase:
        result = result.upper()
    
    return result


